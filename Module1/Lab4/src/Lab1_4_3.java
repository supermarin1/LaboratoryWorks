/**
 * Using logical operators
 */
public class Lab1_4_3 {
    public static void main(String[] args) {

        // Declaring and initializing two variables of boolean data type
        boolean myBool1 = true;
        boolean myBool2 = false;

        // Applying and printing results
        System.out.println("true AND false is " + (myBool1 & myBool2));
        System.out.println("true OR false is " + (myBool1 & myBool2));
        System.out.println("NOT true is " + (myBool1 & myBool2));
        System.out.println("true XOR false " + (myBool1 ^ myBool2));
    }
}
