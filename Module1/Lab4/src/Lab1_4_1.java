/**
 * Declare and initialization variables of primitive data types in Java
 */
public class Lab1_4_1 {
    public static void main(String[] args) {

        // Declaring and initializing of each of primitive data types
        byte myByte = 69;
        short myShort = 1_699;
        char myChar1 = 'S';
        char myChar2 = 77;
        int myInt = 1_956_956_956;
        long myLong = 2_666_999_888L;
        float myFloat = 15.668F;
        double myDouble = 256.256256256;
        boolean myBoolean1 = true;
        boolean myBoolean2 = false;


            System.out.println("myByte is " + myByte);
            System.out.println("myShort is " + myShort);
            System.out.println("myChars are " + myChar1 + " and " + myChar2);
            System.out.println("myInt is " + myInt);
            System.out.println("myLong is " + myLong);
            System.out.println("myFloat is " + myFloat);
            System.out.println("myDouble is " + myDouble);
            System.out.println("All booleans are " + myBoolean1 + " or " + myBoolean2);
    }
}
